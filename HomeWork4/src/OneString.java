public class OneString {
    public void task1 () {
        for (char i = 'a'; i <= 'z'; i++) {
            System.out.print(i + " ");
        }
        System.out.println("");
    }

    public void task2 () {
        for (char i = 'A'; i <= 'Z'; i++) {
            System.out.print(i + " ");
        }
        System.out.println("");
    }

    public void task3 () {
        for (char i = 'а'; i <= 'я'; i++) {
            System.out.print(i + " ");
        }
        System.out.println("");
    }

    public void task4 () {
        for (int i = 0; i <= 9; i++) {
            System.out.print(i + " ");
        }
        System.out.println("");
    }

    public void task5 () {
        for (char i = 32; i <= 126; i++) {
            System.out.print(i + " ");
        }
        System.out.println("");
    }
}
